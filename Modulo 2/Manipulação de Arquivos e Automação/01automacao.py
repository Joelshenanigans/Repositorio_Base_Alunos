# crie uma automacao que abra automaticamente um navegador

# importamos a biblioteca para o script em uso
import pyautogui

# 'press' é um comando para pressionar a tecla desejada
pyautogui.press('win') # para pressionar a tecla windows

# 'sleep' é um comando para deixar o código em espera por segundos
pyautogui.sleep(1)

# 'write' é um comando para passar o que queremos escrever
pyautogui.write('google chrome')

pyautogui.press('enter')










