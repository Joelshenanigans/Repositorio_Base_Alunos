# crie uma lista vazia chamada frutas
# peça para o usuario digitar o nome de uma fruta e use .append 
# para adiciona-las à lista
# mostre a lista no final

frutas = [] # criamos a lista vazia

frutas = input("digite uma fruta: ") # solicitamos ao usuario a informação

frutas.append(frutas) # adicionamos a fruta informada na lista
# fruta informada na lista
print(frutas) # apresentamos a lista





















